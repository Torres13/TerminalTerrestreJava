    private void rellenaTipos() 
    {
        cbUsuario.removeAllItems();
        cbUsuario.addItem(""); 
        cbUsuario.addItem("Admin"); cbUsuario.addItem("Vendedor"); cbUsuario.addItem("Customer Service");
    }

    private int validaUsuario()
    {
        String coontrasena = txbContra.getText();
        if (USUARIO.equals("Admin")) 
        {
            if (coontrasena.equals("admin"))
            {
                new PanelControl(USUARIO).setVisible(true);
                this.dispose();
            }
            else
            {
                JOptionPane.showMessageDialog(null, "Contrase単a Incorrecta ","Error", JOptionPane.ERROR_MESSAGE); 
                cbUsuario.setSelectedIndex(0);
                txbContra.setText("");
                return 0;                
            }                
        }
        if (USUARIO.equals("Vendedor")) 
        {
            if (coontrasena.equals("vendedor"))
            {
                new PanelControl(USUARIO).setVisible(true);
                this.dispose();
            }
            else
            {
                JOptionPane.showMessageDialog(null, "Contrase単a Incorrecta ","Error", JOptionPane.ERROR_MESSAGE); 
                cbUsuario.setSelectedIndex(0);
                txbContra.setText("");
                return 0;                
            }                
        }
        if (USUARIO.equals("Customer Service")) 
        {
            if (coontrasena.equals("customer"))
            {
                new PanelControl(USUARIO).setVisible(true);
                this.dispose();
            }
            else
            {
                JOptionPane.showMessageDialog(null, "Contrase単a Incorrecta ","Error", JOptionPane.ERROR_MESSAGE); 
                cbUsuario.setSelectedIndex(0);
                txbContra.setText("");
                return 0;                
            }                
        }
        return 1;
    }

